    // Este metodo consulta una tabla
    public void agregarProfesionales(){
        // Creo una instacia de operaciones
        Operaciones o = new Operaciones();
        // Guardo en un clase del tipo resultado de consulta la consulta que hice sobre operaciones
        ResultSet resultado = o.consultar("select * from profesional");
        try {
            DefaultTableModel model = (DefaultTableModel) tablaProfesional.getModel();
            while(resultado.next()){
                //Obtengo los elementos de resultado.getString(numero de columna)
                model.addRow(new Object[]{resultado.getString(1),resultado.getString(3), resultado.getString(4)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
